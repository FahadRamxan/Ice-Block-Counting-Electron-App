export function t(locale: Locale, key: string, vars?: Record<string, string | number>): string {
  let s = messages[locale][key] ?? messages.en[key] ?? key;
  if (vars) {
    for (const [k, v] of Object.entries(vars)) {
      s = s.replace(new RegExp(`\\{${k}\\}`, 'g'), String(v));
    }
  }
  return s;
}
